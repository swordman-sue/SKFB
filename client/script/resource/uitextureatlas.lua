--获取贴图集信息
Resource.UITextureAtlas.GetTexAtlasInfo = function(key, language_related)
	if not key then
		LogTool.LogError("[Resource.UITextureAtlas.GetTexAtlasInfo] 参数key无效")
		return
	end
	local config_tex_atlas = Resource.UITextureAtlas[key]
	if not config_tex_atlas then
		LogTool.LogError("[Resource.UITextureAtlas.GetTexAtlasInfo] 找不到Resource.UITextureAtlas[%d]", key)
		return
	end

	local tex_atlas_info = {}
	tex_atlas_info.key = key
	tex_atlas_info.config_key = not language_related and config_tex_atlas.key or string.gsub(config_tex_atlas.key, "Ui_Res", "Ui_Res_Language")
	tex_atlas_info.config_path = Resource.PathTool.GetUICnfPath(config_tex_atlas.cnf_name, language_related)
	tex_atlas_info.texture_path = Resource.PathTool.GetUIResPath(config_tex_atlas.cnf_name, language_related)
	return tex_atlas_info
end